
// break statements in JS
let number = ''
for (let i = 0; i < 10; i++) {
    if (i==5) {
        break
    }
    number +=  ' ' + i ;
}
console.log(number)


// Continue statements in JS

let number1 = ''
for (let i = 0; i < 10; i++) {
    if (i==5) {
       continue
    }
    number1 +=  ' ' + i ;
}
console.log(number1)



